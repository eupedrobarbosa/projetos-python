test = input ('Digite algo: ')

print ('É alfabético?', test.isalpha())
print ('É númerico?', test.isnumeric())
print ('É alfanúmerico?', test.isalnum())
print ('Tem espaço?', test.isspace())
print ('Tem maisúcula?', test.isupper())
print ('Tem minúscula?', test.islower())
print ('É capitalizada?', test.istitle())